package com.practiceproblem.convertjavaobjectintojson;

public class Car {
    // Attributes of the Car class
    public String name;
    public String color;
    public double price;
    public String type;
    public String brand;

    // Constructor to initialize the Car object with specified attributes
    public Car(String name, String color, double price, String type, String brand) {
        this.name = name;
        this.color = color;
        this.price = price;
        this.type = type;
        this.brand = brand;
    }
}